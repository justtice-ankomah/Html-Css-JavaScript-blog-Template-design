<h4 style="color:blue;font-family:curasive;">9. Transcription</h4>
Like data entry, transcription work from home is a field where you have to be careful about the job offers you encounter. There are some work-from-home scam companies that claim to have legitimate transcription jobs–but when you apply, you are turned down and encouraged to sign up for a transcription certification course, at a cost of a couple of hundred dollars. <br/>
<br/>
However, there are plenty of legitimate work-from-home transcription opportunities out there, and this can be a great way for good typists to earn money at home. <br/>
Transcriptionists have to listen to audio files and type what they hear. While this sounds easy, sometimes the audio files are poor quality or the speakers have accents, making it difficult to determine what is being said. You can expect to have to rewind and re-listen to some portions of the audio multiple times before you can accurately transcribe it. <br/>
