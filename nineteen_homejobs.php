<h4 style="color:blue;font-family:curasive;">2. Virtual Assistant</h4>
Virtual assistants are increasing in popularity, which means highly organized and enterprising individuals can make a good living from home with this career. Entrepreneurs and companies alike are seeking out virtual assistants to help them with their daily tasks. All you need is an Internet-connected computer, and you can be a virtual assistant from anywhere. <br/>
To start working as a VA, identify what it is you can and want to do for a client. Start by listing all of the skills that you know how to do, then list all those you can easily learn. Skills can include email management, bookkeeping, social media management, posting blogs, scheduling appointments, transcription, event planning, organizing files, booking travel, and email and calendar organization–and much more. <br/>
You don’t need any training, although you can take various courses to help sharpen your skills. <br/>
