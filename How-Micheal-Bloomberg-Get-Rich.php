<h4 style="color:blue;font-family:curasive;">Wealth </h4>
In March 2009, Forbes reported Bloomberg's wealth at $16 billion, a gain of $4.5 billion over the previous year, the world's biggest increase in wealth from 2008-2009.  At that time, there were only four fortunes in the United States that were larger (although the Walton family, majority owners of Walmart, fortune is split among four people). Bloomberg moved from 142nd to 17th in the Forbes list of the world's billionaires in only two years.  As of February 2020, Bloomberg was the ninth richest person in the world, and his net worth was estimated at $61.9 billion.
