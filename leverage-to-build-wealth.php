<h2> Debt taken on to pay debt is a losing strategy. </h2>
It eventaully spirals our  of control and may damage all hopes of you ever becoming rich. Not only that , it's one of the primary causes of stress-related suicedes and health problems. Bad debt is extremly expensive, so the best way to manage it is to avoid it.
