<h2>The following scenerios highlight two bad debt situations:</h2>
<span style="color:blue;font-size:20px;font-weight:bold;"> Scenerio 1:</span> Suppose you purchase the car of your dreams for R250,000. The bank grants you finance at 15% interest per annum over five years. Your monthly repayement is approximately R6,000. As soon as you drive the car off the dealer's floor, it loses about 20% in value. This is a bad debt situation as you need to cater for a depreciating asset that doesn't generate any passive income.
<br/>
<span style="color:blue;font-size:20px;font-weight:bold;"> Scenerio 2: </span> Suppose you paid for a  R40,000 holiday using your credit card. The debt will be paid over a three year period at 20% interest. Your monthly repayment is almost R1,500. After spending two weeks in Maurituis, you retrun home feeling refreshed and ready to take on the stressful world fo work life. Besides bringing home two weeks worth of great memories, you still have to cough up R1,500 for the next 36 months. This is bad debt situation. 
<br/>
<br/>
How are you curently  managing your debt ? <br/>
Are you managing your debt ? YOu can use the pointers below to assess your own situation:<br/>
1) Be honest with yourself. Are you a conscientious spender or excessive spender ?<br/>
2) Is your total monthly debt higher than your monthly income ? If yes, you're on the road to financial ruin.<br/>
3) Start paying off bad debt today and pay more than the minimum amount required each month. Cut up all your credit cards, except one which will be used for emergencies only.<br/>
4)A debt counseller may help you set up a plan to  eliminate your bad debt.<br/>
5) Track your spending and budget. If need be, file all your slips and invoices no matter how big or small. <br/>
6) Downsize and get real. You cannot live in a massive house, Use expensive toys or lead a luxurious lifestyle if you have limited income
You will be  surprised at how much cash you can free up by down sizing.<br/>
7) Devise a plan to acquire good debt and income generating assets.
