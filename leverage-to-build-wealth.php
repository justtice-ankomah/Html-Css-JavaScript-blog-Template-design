<h4>GOOD DEBT :</h4> Is money that is borrowed from other people or institution to purchase income generating assets. It may be a loan to 
start a business or purchase property. The rich use good debt as a tool to generate enormouse wealth. However, it's a double edge sword. It can bring
you great returns or kill your cash flow if your're not careful.<br/>
Here's why good debt is so powerful.<br/>
Suppose a property investor is interested in buying a flat for R100,000. He (or she) has the option of paying cash or borrowing money
from the bank (i.e using leverage). Let's assume he purchased the property with cash.<br/>
What is his return after one year if he makes a profit of R20,000. His return is 20% (20,000 / 100,000). Suppose our investor
pays a R10,000 deposit and is granted a R90,000 bond. What do you think his return is now ? <br/>
After one year his profit is R12,000(R12,000 / 10,000). Not bad considering that you're likely  to get 13% from the stock market
in the long term. Do you see how financial leverage boosts the return on your money ? This is what you're after-growth.
