Open a Side Business </h4>
One way to increase your income is to open a side business that you enjoy. This will likely start out as something that you do part-time while you are still working at your original job, but if you are smart about it, you may be able to grow it into something that you do full-time.  Find something that you really enjoy doing or that you believe in and try to start working at it. It helps to have a business plan. You will also want to make sure that you are not putting more money into the business than you are taking out.
