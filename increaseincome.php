<h4 style="color:blue;font-family:curasive;">2.
Go Back to School </h4>
Another option is to go back to school. With most degrees, you can increase your earning power within your profession as you move up from bachelor’s to master’s to a doctorate. Not all fields are this way, and you want to make sure you are studying a field that will give you a return on the money and time that you are putting into your education. You may also want to see if you qualify for a tuition reimbursement program through your work, which can make going back to school easier. 

If going back to school for an additional degree is not a good fit, consider going back to get additional certifications for your current field of work. This may be less time consuming and less expensive and help you earn more money.
