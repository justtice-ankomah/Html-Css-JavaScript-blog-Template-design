<h4 style="color:blue;font-family:curasive;">3.
Make Money with Your Hobbies </h4>
You can put the things you love doing in your free time to good use. There are a lot of ways to cash in on your hobbies, and if you are creative, you can make quite a bit of money in your free time. You can start by opening an Etsy store to sell your crafts or artwork. Be sure that you charge enough to cover both your materials and time. You can also sell patterns or designs if you can come up with them on your own.

Another way to profit from your hobbies is to create a YouTube channel or show based on the hobby. You can show how to make the items or review items that you use on a regular basis. If your hobby is gaming, you can even do videos of walkthrough and commentary with your friends. It can take time to build a following.  However, if you stick with it and are consistent this may turn into a great way to make extra money.
