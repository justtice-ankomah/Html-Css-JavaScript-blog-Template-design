Don’t wait until retirement age to start saving or investing. DUH! <br/>
But seriously, you need to start thinking about your future and how you’re going to afford the lifestyle you want. <br/>
As business owners, we’re more tech-savvy and have access to awesome products and services that make our financial better. <br/>
<h4style="color:blue;font-family:curasive;">Don’t skimp on investing in your future!</h4>
One example of how to invest in your future is with Acorns. Acorns is an investment app that rounds up your spending and invests the difference. <br/>
It’s amazing what a few cents can do once you add it all up throughout the month!
You open a free account, set up your first $5 or $10 transfer and you’re on your way to building wealth.
But even if you don’t use an app like Acorns, set up automatic withdrawals from your checking account into the your investment account each month. <br/>
Don’t wait to start saving for a better life in retirement.
<br/><br/>
<h4 style="color:blue;font-family:curasive;">4. Never stop learning</h4>
Each of us will go through phases in our businesses where we aren’t as busy as we’d like, that’s the perfect time to continue learning. You can start feeling a bit restless or bored, and this can lead to burn out. <br/>
Take a moment to think…
What’s a skill you’ve been dying to learn? What’s something in your biz that you’re frustrated with that you know could be better?
Look at an area of your business that you might be under-utilizing, like Pinterest. Consider taking a Pinterest course and start implementing new marketing strategies.
Have you been interested in learning a skill, like CSS? Start learning for free online and building your own site from scratch. <br/><br/>
