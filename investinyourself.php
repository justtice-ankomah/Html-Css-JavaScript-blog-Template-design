<h5 style="color:darkorange; padding:20px;background-color:#f2f2f2;border-radius:10px;">	Learning how to invest in yourself can be a bit of a tug-of-war, especially as a business owner.<br/>              
You want to invest in self-care but if you’re not working on your business, you’re not making money. This can lead to feeling so guilt, stress, and anxiety. <br/>
It’s not a pretty cycle to get stuck in. ???? <br/>
However, these are the things that take time to understand, perfect and turn into consistent habits. By the time you’re done reading this you will actually be excited about investing! <br/>
Why? <br/>
Because there’s a simple investment that you will actually enjoy and see big returns. What is it? <br/>
Yourself! “ The best investment you can make, is in YOU.”  Here are ten ways to invest in yourself and watch your life change for the better.</h5>
<h4 style="color:blue;font-family:curasive;" >1. Read books and blogs<h4>
