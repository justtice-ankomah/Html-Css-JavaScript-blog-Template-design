No one can take advantage of you if you don’t let them. That’s one of the biggest lessons I’ve learned as a business owner — sometimes saying NO is just as important as saying YES. <br/>
You must learn to say YES and NO to the right things and create boundaries within your life. And just because you say NO to something doesn’t make you a bad person. <br/>
In fact, knowing your limits and respecting them is what will make you a great person!
Understanding your strengths and knowing your weaknesses will make you stronger individual and business owner. <br/>
