You already know the importance of self-care but how often do you actually make time for this in your daily life? <br/>
From taking a break to watch Netflix (anyone binge watch #Girlboss like I did?) to simply enjoying a bubble bath, giving yourself a break is key to avoiding burnout. <br/>
One thing I like to do to take a break is simply get outside, and sometimes it’s for an entire weekend. A weekend getaway is a great time to recharge and reinvigorate yourself as a person — not just a business owner. <br/>
Give yourself permission to take a break and don’t feel guilty about doing so! 
