When we asked Mike Handlesman if it really is possible to buy a small business with no money down, that is, with 100% seller financing, he answered that is possible, but it won’t be easy, noting, “Most sellers have been waiting for 20 years to retire and won’t want to wait longer to cash out.” But you never know; you just might find someone willing to help you get started if they see that you will be able to pay them off in a few years.<br/>
