If you want full or partial seller financing, the expert gave these final tips: <br/><br/>
•	“Show that you have a high likelihood of success” – you have experience, contacts, etc. <br/>
•	“Offer aggressive terms” – a 3 year note, that sort of thing. <br/>
•	“Work with a broker.” <br/>
•	“Engage the seller as a consultant for six to 12 months” – that way, you up their income. <br/>
