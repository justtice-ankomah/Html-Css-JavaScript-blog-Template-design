    <h3 style="color:blue;"> ARE YOU SURE THIS IS A GOOD ADVICE ? </H3>
Is it possible to buy a business with no money down? Yes, indeed, in The Small Business Bible, Steve Strauss discusses just how to do that. The key is to get the seller to finance 100%. We will look at 100% seller financing  to do that below, but first, it is important to consider whether buying a business at all is right for you.<br/>
<br/>
<h4 style="color:blue;font-family:curasive;"> Can you really buy a business with no money down? Yes, you can. It’s not easy, but it can be done.?</4>
As a general rule, buying a preexisting business can be a very good idea, but as with everything else in this economy, to make sure it will be a successful venture, you have to proceed with caution, do your due diligence, and understand the pros and cons.<br/>
First the pros: With a preexisting small business, you should have a pretty good idea of what you are getting. The business makes X number of dollars in profit a year, has Y number of customers, and so on. The kinks should mostly be ironed out and the brand is already established. All good, <br/><br/>
