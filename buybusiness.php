<h4 style="color:blue;font-family:curasive;"> J Potential Pitfalls to Buying a Business</h4>
1.	First, you have to make sure that the success of the business is not due to the current owner. If the customers, for instance,are his personally and not really those of the business, that is a red flag.<br/>
<br/>
<br/>
2.	Similarly, you need to be extra certain, given the economic times we are in, that the business will remain as profitable as it has been in the last few years.<br/>
