So that is the key to buying a business with 100% seller financing — finding a seller who really needs to sell. Look for a business that has been on the market for a long time; in that sense, it’s not unlike getting a  good deal on a house that has been on the market too long. Check Craigslist under Businesses for Sale, as well as BizBuySell. <br/>
<br/>
<br/>
If you do find a what looks to be a viable business for sale, then the key is to speak with your attorney. Have him or her do a background check on the business and the owner. Do your due diligence. But also, don’t expect to get a great deal – a seller who finances the sale will not negotiate beyond that in all likelihood. <br/>
“But remember” said Handlesman, “the danger of finding a seller willing to do 100% financing is that the business may not be very viable.” <br/>
