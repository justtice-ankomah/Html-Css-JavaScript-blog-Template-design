<h4 style="color:blue;font-family:curasive;">4. Trust your gut.</h4>
<p style="color:orange;font-family:curasive;"><strong>What I Wish I Knew When I Was 20, in terms of "Self Trust"</strong></p>
<p></p>
If you’re dating someone and don’t fully trust them or know it won’t go anywhere but just want to give the guy (or girl) a shot because they’re so “great” (except they’re not), walk away. Don’t waste your time on people who don’t deserve it.<br/><br/>
<h4 style="color:blue;font-family:curasive;">5. Sleep in.</h4>
<p style="color:orange;font-family:curasive;"><strong>What I Wish I Knew When I Was 20, in terms of "Sleeping"</strong></p>
<p></p>
I used to go out until 2 am and sleep in until 11. Then I entered my 30s and sleeping until 9 became really, really late. Enjoy sleeping in while you can.<br/><br/>
