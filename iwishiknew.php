<h4 style="color:blue;font-family:curasive;">7. Learn to spend time with yourself.</h4>
<p style="color:orange;font-family:curasive;"><strong>What I Wish I Knew When I Was 20, in terms of "Leisure time"</strong></p>
<p></p>
Before moving to Chicago, I was always really bad at doing things on my own. I was never really the go-out-to-lunch-alone type simply because I’d rather share that experience with someone. Tried it, and it wasn’t for me.
<p></p>
 But spending 4-5 years living on my own taught me to learn to love days and nights to myself. Morning walks by the lake, cooking, hanging with Buddy, and sole possession of the remote control.<br/><br/>
