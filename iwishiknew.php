<h4 style="color:blue;font-family:curasive;">8. Take care of yourself.</h4>
<p style="color:orange;font-family:curasive;"><strong>What I Wish I Knew When I Was 20, in terms of "Health"</strong></p>
	<p></p>
Okay, so I did most of this but it’s such an important reminder that I’m going to say it anyway. You’re young and healthy, right? Great. Don’t skip your annual skin check with the dermatologist (I had a pre-cancerous mole removed in my 20s!), see your dentist twice a year, and don’t forget your annual physical. Splurging on your favorite treat is fine, but follow a mostly healthy diet and don’t skip meals — but do skip all those soft drinks.<br/><br/>
