<h4 style="color:blue;font-family:curasive;">3. Relationships shouldn’t be so difficult.</h4>
<p style="color:orange;font-family:curasive;"><strong>What I Wish I Knew When I Was 20, in terms of "Relationship"</strong></p>
<p></p>
This goes for all kinds of relationships. If you have one of those really difficult friends you’re always fighting with, it’s time to reevaluate that friendship. I’ve talked about this before, but I spent most of my 20s in a rocky, up-and-down, verbally abusive relationship.
<p></p>
 We broke up and got back together too many times to count. Looking back, I can’t believe I gave that guy more than five minutes. If you have to try really hard to get along or make things work before life gets really real, it’s time to call it quits.<br/>
