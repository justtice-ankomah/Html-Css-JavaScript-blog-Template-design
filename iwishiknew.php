<h4 style="color:blue;font-family:curasive;"> 1. Everything is going to be ok.</h4>
<p style="color:orange;font-family:curasive;"><strong>What I Wish I Knew When I Was 20, in terms of "The Future"</strong></p>
<p></p>
I spent most of my 20s worrying about the future. Where my career would go and when I’d get married and have kids. I wish I had worried less, embraced where I was, and that I knew everything was going to be better than okay.<br/><br/>
<h4 style="color:blue;font-family:curasive;">2. Timelines are stupid.</h4>
<p style="color:orange;font-family:curasive;"><strong>What I Wish I Knew When I Was 20, in terms of "Time"</strong></p>
<p></p>
Learning to let go of timelines was a tough one. I struggled through this until my early 30s and thought I had to be married by 28 and have my first baby by 30. I got married at 35 and will be having a baby a few months before my 36th birthday. 
<p></p>
It’s crazy to think that the way my life turned out is what I was afraid of. I moved from LA to Chicago, started and grew a business, and spent years living on my own. I did not want to get married and have a baby at 35 (my self-proclaimed “scary age”), but that’s exactly what was in the cards for me, and it’s not scary at all.<br/><br/>
