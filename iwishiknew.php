<h4 style="color:blue;font-family:curasive;">9. Slow down.</h4>
<p style="color:orange;font-family:curasive;"><strong>What I Wish I Knew When I Was 20, in terms of "Doing Thing Quick"</strong></p>
<p></p>
It’s so easy to keep going out when you don’t feel up for it, to say yes to that extra project, and to keep pushing until you get shingles (those are still a thing, trust me). I wish I had been a regular CrossFitter and not an overzealous one who tracked every score and had to work out five times a week.<br/><br/>
