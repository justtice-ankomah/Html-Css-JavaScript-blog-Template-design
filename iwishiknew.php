<h4 style="color:blue;font-family:curasive;">6. Stick to a budget.</h4>
<p style="color:orange;font-family:curasive;"><strong>What I Wish I Knew When I Was 20, in terms of "Finance and Budget"</strong></p>
<p></p>
At 28, my rent was about $300 more than it should have been. My graphic design business made enough to pay for the essentials, but I bought stupid outfits (there was a pink striped sequin shirt) and went out with friends. 
<p title="What I Wish I Knew When I Was 20"></p>
I never bought anything I couldn’t pay off at the end of the month, but could have done a much better job at budgeting and putting even a little money away. Work toward your goals, but don’t freak out if you’re not making as much as others around you.<br/><br/>
